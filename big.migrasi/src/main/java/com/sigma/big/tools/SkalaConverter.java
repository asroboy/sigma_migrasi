package com.sigma.big.tools;

/**
 *
 * @author Ridho
 */
public class SkalaConverter {

    String K1 = "1000";
    String K5 = "5000";
    String K2_5 = "2500";
    String K10 = "10000";
    String K25 = "25000";
    String K50 = "50000";
    String K100 = "100000";
    String K250 = "250000";
    String K500 = "500000";
    String M1 = "1000000";

    public String convertSkala(String skala) {
        if (skala.equals(K1)) {
            return "1K";
        }
        if (skala.equals(K5)) {
            return "5K";
        }
        if (skala.equals(K2_5)) {
            return "2,5K";
        }
        if (skala.equals(K10)) {
            return "10K";
        }
        if (skala.equals(K25)) {
            return "25K";
        }
        if (skala.equals(K50)) {
            return "25K";
        }
        if (skala.equals(K100)) {
            return "100K";
        }
        if (skala.equals(K250)) {
            return "250K";
        }
        if (skala.equals(K500)) {
            return "500K";
        }
        if (skala.equals(M1)) {
            return "1M";
        }
        return "";
    }
}

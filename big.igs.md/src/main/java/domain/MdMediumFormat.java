package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class MdMediumFormat implements java.io.Serializable{
    
       
    private BigDecimal mdMediumId;
    private String mediumFormat;

    public MdMediumFormat() {
    }

    public MdMediumFormat(BigDecimal mdMediumId) {
        this.mdMediumId = mdMediumId;
    }

    public MdMediumFormat(BigDecimal mdMediumId, String mediumFormat) {
        this.mdMediumId = mdMediumId;
        this.mediumFormat = mediumFormat;
    }

    public BigDecimal getMdMediumId() {
        return mdMediumId;
    }

    public void setMdMediumId(BigDecimal mdMediumId) {
        this.mdMediumId = mdMediumId;
    }

    public String getMediumFormat() {
        return mediumFormat;
    }

    public void setMediumFormat(String mediumFormat) {
        this.mediumFormat = mediumFormat;
    }
    
}

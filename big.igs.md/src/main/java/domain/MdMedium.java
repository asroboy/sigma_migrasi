package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class MdMedium implements java.io.Serializable{
    
    private BigDecimal id;
    private String densityUnits;
    private String mediumNote;
    private BigDecimal volumes;
    private BigDecimal mdDigitalTransferOptionsId;
    private String name;

    public MdMedium() {
    }

    public MdMedium(BigDecimal id) {
        this.id = id;
    }

    public MdMedium(BigDecimal id, String densityUnits, String mediumNote, BigDecimal volumes, BigDecimal mdDigitalTransferOptionsId, String name) {
        this.id = id;
        this.densityUnits = densityUnits;
        this.mediumNote = mediumNote;
        this.volumes = volumes;
        this.mdDigitalTransferOptionsId = mdDigitalTransferOptionsId;
        this.name = name;
    }

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public String getDensityUnits() {
        return densityUnits;
    }

    public void setDensityUnits(String densityUnits) {
        this.densityUnits = densityUnits;
    }

    public String getMediumNote() {
        return mediumNote;
    }

    public void setMediumNote(String mediumNote) {
        this.mediumNote = mediumNote;
    }

    public BigDecimal getVolumes() {
        return volumes;
    }

    public void setVolumes(BigDecimal volumes) {
        this.volumes = volumes;
    }

    public BigDecimal getMdDigitalTransferOptionsId() {
        return mdDigitalTransferOptionsId;
    }

    public void setMdDigitalTransferOptionsId(BigDecimal mdDigitalTransferOptionsId) {
        this.mdDigitalTransferOptionsId = mdDigitalTransferOptionsId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}

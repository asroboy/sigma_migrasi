package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class CiCitationPresentationForm implements java.io.Serializable{
    
    private BigDecimal ciCitationId;
    private String presentationForm;

    public CiCitationPresentationForm() {
    }

    public CiCitationPresentationForm(BigDecimal ciCitationId, String presentationForm) {
        this.ciCitationId = ciCitationId;
        this.presentationForm = presentationForm;
    }

    public BigDecimal getCiCitationId() {
        return ciCitationId;
    }

    public void setCiCitationId(BigDecimal ciCitationId) {
        this.ciCitationId = ciCitationId;
    }

    public String getPresentationForm() {
        return presentationForm;
    }

    public void setPresentationForm(String presentationForm) {
        this.presentationForm = presentationForm;
    }
    
    
}

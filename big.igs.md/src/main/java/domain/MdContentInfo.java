package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class MdContentInfo implements java.io.Serializable{
    
    private String extendsType;
    private BigDecimal id;
    private BigDecimal mdMetadataId;

    public MdContentInfo() {
    }

    public MdContentInfo(BigDecimal id) {
        this.id = id;
    }

    public MdContentInfo(String extendsType, BigDecimal id, BigDecimal MdMetadataId) {
        this.extendsType = extendsType;
        this.id = id;
        this.mdMetadataId = MdMetadataId;
    }

    public String getExtendsType() {
        return extendsType;
    }

    public void setExtendsType(String extendsType) {
        this.extendsType = extendsType;
    }

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public BigDecimal getMdMetadataId() {
        return mdMetadataId;
    }

    public void setMdMetadataId(BigDecimal MdMetadataId) {
        this.mdMetadataId = MdMetadataId;
    }
    
}

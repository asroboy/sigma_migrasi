package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class MdFeatureCatalogueDescType implements java.io.Serializable{
    
    private BigDecimal mdFeatureCatalogueDescId;
    private String featureTypes;

    public MdFeatureCatalogueDescType() {
    }

    public MdFeatureCatalogueDescType(BigDecimal mdFeatureCatalogueDescId) {
        this.mdFeatureCatalogueDescId = mdFeatureCatalogueDescId;
    }

    public MdFeatureCatalogueDescType(BigDecimal mdFeatureCatalogueDescId, String featureTypes) {
        this.mdFeatureCatalogueDescId = mdFeatureCatalogueDescId;
        this.featureTypes = featureTypes;
    }

    public BigDecimal getMdFeatureCatalogueDescId() {
        return mdFeatureCatalogueDescId;
    }

    public void setMdFeatureCatalogueDescId(BigDecimal mdFeatureCatalogueDescId) {
        this.mdFeatureCatalogueDescId = mdFeatureCatalogueDescId;
    }

    public String getFeatureTypes() {
        return featureTypes;
    }

    public void setFeatureTypes(String featureTypes) {
        this.featureTypes = featureTypes;
    }
     
     
}

package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class MdIdentifier implements java.io.Serializable{
    
    private BigDecimal id;
    private String extendsType;
    private String code;
    private BigDecimal mdAggregateInfoId;
    private BigDecimal ciCitationId;
    private BigDecimal dqElementId;
    private BigDecimal exGeographicDescriptionId;
    private BigDecimal mdImageDescriptionIqc;
    private BigDecimal mdImageDescriptionPlc;

    public MdIdentifier() {
    }

    public MdIdentifier(BigDecimal id) {
        this.id = id;
    }

    public MdIdentifier(BigDecimal id, String extendsType, String code, BigDecimal mdAggregateInfoId, BigDecimal ciCitationId, BigDecimal dqElementId, BigDecimal exGeographicDescriptionId, BigDecimal mdImageDescriptionIqc, BigDecimal mdImageDescriptionPlc) {
        this.id = id;
        this.extendsType = extendsType;
        this.code = code;
        this.mdAggregateInfoId = mdAggregateInfoId;
        this.ciCitationId = ciCitationId;
        this.dqElementId = dqElementId;
        this.exGeographicDescriptionId = exGeographicDescriptionId;
        this.mdImageDescriptionIqc = mdImageDescriptionIqc;
        this.mdImageDescriptionPlc = mdImageDescriptionPlc;
    }
 
    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public String getExtendsType() {
        return extendsType;
    }

    public void setExtendsType(String extendsType) {
        this.extendsType = extendsType;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public BigDecimal getMdAggregateInfoId() {
        return mdAggregateInfoId;
    }

    public void setMdAggregateInfoId(BigDecimal mdAggregateInfoId) {
        this.mdAggregateInfoId = mdAggregateInfoId;
    }
    

    public BigDecimal getCiCitationId() {
        return ciCitationId;
    }

    public void setCiCitationId(BigDecimal ciCitationId) {
        this.ciCitationId = ciCitationId;
    }

    public BigDecimal getDqElementId() {
        return dqElementId;
    }

    public void setDqElementId(BigDecimal dqElementId) {
        this.dqElementId = dqElementId;
    }

    public BigDecimal getExGeographicDescriptionId() {
        return exGeographicDescriptionId;
    }

    public void setExGeographicDescriptionId(BigDecimal exGeographicDescriptionId) {
        this.exGeographicDescriptionId = exGeographicDescriptionId;
    }

    public BigDecimal getMdImageDescriptionIqc() {
        return mdImageDescriptionIqc;
    }

    public void setMdImageDescriptionIqc(BigDecimal mdImageDescriptionIqc) {
        this.mdImageDescriptionIqc = mdImageDescriptionIqc;
    }

    public BigDecimal getMdImageDescriptionPlc() {
        return mdImageDescriptionPlc;
    }

    public void setMdImageDescriptionPlc(BigDecimal mdImageDescriptionPlc) {
        this.mdImageDescriptionPlc = mdImageDescriptionPlc;
    }
    
    
    
}

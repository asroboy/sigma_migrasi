package domain;

import java.math.BigDecimal;

/**
 *
 * @author wallet
 */
public class MdConstraintsUseLimitation implements java.io.Serializable{
    
    private BigDecimal mdConstraintsId;
    private String useLimitation;

    public MdConstraintsUseLimitation() {
    }

    public MdConstraintsUseLimitation(BigDecimal mdConstraintsId, String useLimitation) {
        this.mdConstraintsId = mdConstraintsId;
        this.useLimitation = useLimitation;
    }

    public BigDecimal getMdConstraintsId() {
        return mdConstraintsId;
    }

    public void setMdConstraintsId(BigDecimal mdConstraintsId) {
        this.mdConstraintsId = mdConstraintsId;
    }

    public String getUseLimitation() {
        return useLimitation;
    }

    public void setUseLimitation(String useLimitation) {
        this.useLimitation = useLimitation;
    }
    
    
}

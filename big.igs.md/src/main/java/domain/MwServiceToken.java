package domain;

import java.math.BigDecimal;
import java.util.Date;

/**
 *
 * @author wallet
 */
public class MwServiceToken implements java.io.Serializable{
    
    private BigDecimal id;
    private String token;
    private String status;
    private Date loginAt;
    private Date logoutAt;
    private Date lastAccess;
    private BigDecimal serviceUserIdFk;

    public MwServiceToken() {
    }

    public MwServiceToken(BigDecimal id) {
        this.id = id;
    }

    public MwServiceToken(BigDecimal id, String token, String status, Date loginAt, Date logoutAt, Date lastAccess, BigDecimal serviceUserIdFk) {
        this.id = id;
        this.token = token;
        this.status = status;
        this.loginAt = loginAt;
        this.logoutAt = logoutAt;
        this.lastAccess = lastAccess;
        this.serviceUserIdFk = serviceUserIdFk;
    }
   
    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Date getLoginAt() {
        return loginAt;
    }

    public void setLoginAt(Date loginAt) {
        this.loginAt = loginAt;
    }

    public Date getLogoutAt() {
        return logoutAt;
    }

    public void setLogoutAt(Date logoutAt) {
        this.logoutAt = logoutAt;
    }

    public Date getLastAccess() {
        return lastAccess;
    }

    public void setLastAccess(Date lastAccess) {
        this.lastAccess = lastAccess;
    }

    public BigDecimal getServiceUserIdFk() {
        return serviceUserIdFk;
    }

    public void setServiceUserIdFk(BigDecimal serviceUserIdFk) {
        this.serviceUserIdFk = serviceUserIdFk;
    }
    
            
}

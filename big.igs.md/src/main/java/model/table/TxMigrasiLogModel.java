package model.table;

import java.math.BigDecimal;
import java.sql.Clob;

/**
 *
 * @author wallet
 */
public class TxMigrasiLogModel {
    
    private BigDecimal id;
    private String idMigrasi;
    private BigDecimal idDataSet;
    private BigDecimal idRow;
    private BigDecimal status;
    private Clob msgError;

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public String getIdMigrasi() {
        return idMigrasi;
    }

    public void setIdMigrasi(String idMigrasi) {
        this.idMigrasi = idMigrasi;
    }

    public BigDecimal getIdDataSet() {
        return idDataSet;
    }

    public void setIdDataSet(BigDecimal idDataSet) {
        this.idDataSet = idDataSet;
    }

    public BigDecimal getIdRow() {
        return idRow;
    }

    public void setIdRow(BigDecimal idRow) {
        this.idRow = idRow;
    }

    public BigDecimal getStatus() {
        return status;
    }

    public void setStatus(BigDecimal status) {
        this.status = status;
    }

    public Clob getMsgError() {
        return msgError;
    }

    public void setMsgError(Clob msgError) {
        this.msgError = msgError;
    }
    
    
}

package model.table;

import java.math.BigDecimal;
import java.sql.Clob;

/**
 *
 * @author wallet
 */
public class TrKelompokLayerModel {
    
    private BigDecimal id;
    private String nama;
    private Clob deskripsi;
    private BigDecimal jnsLayer;

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public Clob getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(Clob deskripsi) {
        this.deskripsi = deskripsi;
    }

    public BigDecimal getJnsLayer() {
        return jnsLayer;
    }

    public void setJnsLayer(BigDecimal jnsLayer) {
        this.jnsLayer = jnsLayer;
    }
    
}

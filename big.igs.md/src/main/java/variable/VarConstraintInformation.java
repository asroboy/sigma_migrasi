package variable;

/**
 *
 * @author wallet
 */
public class VarConstraintInformation {
    
    private String useLimitation;
    private String accessConstraints;
    private String useConstraints;
    private String otherConstraints;
    private String classification;
    private String userNote;
    private String classificationSystem;
    private String handlingDescription;

    public String getUseLimitation() {
        return useLimitation;
    }

    public void setUseLimitation(String useLimitation) {
        this.useLimitation = useLimitation;
    }

    public String getAccessConstraints() {
        return accessConstraints;
    }

    public void setAccessConstraints(String accessConstraints) {
        this.accessConstraints = accessConstraints;
    }

    public String getUseConstraints() {
        return useConstraints;
    }

    public void setUseConstraints(String useConstraints) {
        this.useConstraints = useConstraints;
    }

    public String getOtherConstraints() {
        return otherConstraints;
    }

    public void setOtherConstraints(String otherConstraints) {
        this.otherConstraints = otherConstraints;
    }

    public String getClassification() {
        return classification;
    }

    public void setClassification(String classification) {
        this.classification = classification;
    }

    public String getUserNote() {
        return userNote;
    }

    public void setUserNote(String userNote) {
        this.userNote = userNote;
    }

    public String getClassificationSystem() {
        return classificationSystem;
    }

    public void setClassificationSystem(String classificationSystem) {
        this.classificationSystem = classificationSystem;
    }

    public String getHandlingDescription() {
        return handlingDescription;
    }

    public void setHandlingDescription(String handlingDescription) {
        this.handlingDescription = handlingDescription;
    }
    
    
    
}

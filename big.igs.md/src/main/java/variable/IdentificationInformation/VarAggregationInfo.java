public class VarAggregationInfo extends VarCitation{
    
    private String titleIdentifier;
    private String dateIdentifier;
    private String dateTypeIdentifier;
    private String aggregateDataSetIdentifier;
    private String DS_AssociationTypeCode;
    private String DS_InitiativeTypeCode;

    public String getAggregateDataSetIdentifier() {
        return aggregateDataSetIdentifier;
    }

    public void setAggregateDataSetIdentifier(String aggregateDataSetIdentifier) {
        this.aggregateDataSetIdentifier = aggregateDataSetIdentifier;
    }


    public String getDS_AssociationTypeCode() {
        return DS_AssociationTypeCode;
    }

    public void setDS_AssociationTypeCode(String DS_AssociationTypeCode) {
        this.DS_AssociationTypeCode = DS_AssociationTypeCode;
    }

    public String getDS_InitiativeTypeCode() {
        return DS_InitiativeTypeCode;
    }

    public void setDS_InitiativeTypeCode(String DS_InitiativeTypeCode) {
        this.DS_InitiativeTypeCode = DS_InitiativeTypeCode;
    }

    public String getTitleIdentifier() {
        return titleIdentifier;
    }

    public void setTitleIdentifier(String titleIdentifier) {
        this.titleIdentifier = titleIdentifier;
    }

    public String getDateIdentifier() {
        return dateIdentifier;
    }

    public void setDateIdentifier(String dateIdentifier) {
        this.dateIdentifier = dateIdentifier;
    }

    public String getDateTypeIdentifier() {
        return dateTypeIdentifier;
    }

    public void setDateTypeIdentifier(String dateTypeIdentifier) {
        this.dateTypeIdentifier = dateTypeIdentifier;
    }
    
}

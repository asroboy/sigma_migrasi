package variable.IdentificationInformation;

/**
 *
 * @author WIN8
 */
public class VarExtent {
    
    private String description;
    private String extentTypeCode;
    private String westBoundLongitude;
    private String eastBoundLongitude;
    private String southBoundLatitude;
    private String northBoundLatitude;
    private String temporalElement;
    private String verticalElement;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getExtentTypeCode() {
        return extentTypeCode;
    }

    public void setExtentTypeCode(String extentTypeCode) {
        this.extentTypeCode = extentTypeCode;
    }

    public String getWestBoundLongitude() {
        return westBoundLongitude;
    }

    public void setWestBoundLongitude(String westBoundLongitude) {
        this.westBoundLongitude = westBoundLongitude;
    }

    public String getEastBoundLongitude() {
        return eastBoundLongitude;
    }

    public void setEastBoundLongitude(String eastBoundLongitude) {
        this.eastBoundLongitude = eastBoundLongitude;
    }

    public String getSouthBoundLatitude() {
        return southBoundLatitude;
    }

    public void setSouthBoundLatitude(String southBoundLatitude) {
        this.southBoundLatitude = southBoundLatitude;
    }

    public String getNorthBoundLatitude() {
        return northBoundLatitude;
    }

    public void setNorthBoundLatitude(String northBoundLatitude) {
        this.northBoundLatitude = northBoundLatitude;
    }

    public String getTemporalElement() {
        return temporalElement;
    }

    public void setTemporalElement(String temporalElement) {
        this.temporalElement = temporalElement;
    }

    public String getVerticalElement() {
        return verticalElement;
    }

    public void setVerticalElement(String verticalElement) {
        this.verticalElement = verticalElement;
    }
    
    
}

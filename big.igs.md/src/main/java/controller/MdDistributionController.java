package controller;

import config.HibernateUtil;
import config.HibernateUtilXml;
import domain.MdDistribution;
import domain.MdDistribution;
import java.math.BigDecimal;
import java.util.List;
import model.table.MdDistributionModel;
import model.table.MdDistributionModel;
import org.hibernate.Criteria;
import org.hibernate.Session;
import org.hibernate.Transaction;
import org.hibernate.criterion.Order;
import org.hibernate.criterion.Restrictions;

/**
 *
 * @author Fauzy Ramedia
 */
public class MdDistributionController {

    HibernateUtilXml hibernateUtilXml;
    Session session;
    
    public MdDistributionController() {
    }

    public MdDistributionController(Session session, HibernateUtilXml hibernateUtilXml) {
        this.hibernateUtilXml = hibernateUtilXml;
        this.session = session;
    }

    public BigDecimal getMaxMdDistributionId() {

        BigDecimal maxId = null;

        if (!session.isOpen()) {
            session = hibernateUtilXml.buildSession().openSession();
        }
        Transaction trx = session.beginTransaction();

        try {
            if (!trx.isActive()) {
                trx.begin();
            }

            Criteria criteria = session.createCriteria(MdDistribution.class);
            criteria.addOrder(Order.desc("id"));
            criteria.setMaxResults(1);
            List results = criteria.list();

            if (results.size() > 0) {
                maxId = ((MdDistribution) results.get(0)).getId();
            } else {
                //  maxId = 0;
            }

            trx.commit();

        } catch (Exception e) {
            trx.rollback();
            e.printStackTrace();
        }
        //session.close();

        return maxId;
    }

    public String saveMdDistribution(MdDistributionModel mdModel) {
        MdDistribution MdDistribution = new MdDistribution();

        if (!session.isOpen()) {
            session = hibernateUtilXml.buildSession().openSession();
        }
        Transaction trx = session.getTransaction();

        try {
            if (!trx.isActive()) {
                trx.begin();
            }

            MdDistribution.setId(mdModel.getId());
            MdDistribution.setMdMetadataId(mdModel.getMdMetadataId());

            session.save(MdDistribution);
            trx.commit();

            return "berhasil disimpan.....!!";
        } catch (Exception e) {
            trx.rollback();
            return "Error "+e.getMessage();
        } finally {
            //session.close();
        }
    }

    public MdDistribution getDataById(BigDecimal Id) {

        MdDistribution md = new MdDistribution();

        if (!session.isOpen()) {
            session = hibernateUtilXml.buildSession().openSession();
        }
        Transaction trx = session.getTransaction();

        try {
            if (!trx.isActive()) {
                trx.begin();
            }

            Criteria criteria = session.createCriteria(MdDistribution.class);
            criteria.add(Restrictions.eq("mdMetadataId", Id));
            md = (MdDistribution) criteria.uniqueResult();

            if (md == null) {
                return null;
            }

            trx.commit();
        } catch (Exception e) {
            trx.rollback();
            e.printStackTrace();
        } finally {
            //session.close();
        }

        return md;
    }
    
    public String deletedMdDistribution(BigDecimal Id) {

        MdDistribution md = new MdDistribution();

        if (!session.isOpen()) {
            session = hibernateUtilXml.buildSession().openSession();
        }
        Transaction trx = session.beginTransaction();

        try {
            if (!trx.isActive()) {
                trx.begin();
            }

            Criteria criteria = session.createCriteria(MdDistribution.class);
            criteria.add(Restrictions.eq("id", Id));
            md = (MdDistribution) criteria.uniqueResult();

            session.delete(md);
            trx.commit();
            
            return "berhasil dihapus.....!!";
        } catch (Exception e) {
            trx.rollback();
            return "Error "+e.getMessage();
        } finally {
            //session.close();
        }
    }

    public String updateMdDistribution(BigDecimal id,MdDistributionModel mdModel) {

        MdDistribution md = new MdDistribution();

        if (!session.isOpen()) {
            session = hibernateUtilXml.buildSession().openSession();
        }
        Transaction trx = session.beginTransaction();

        try {
            if (!trx.isActive()) {
                trx.begin();
            }

            Criteria criteria = session.createCriteria(MdDistribution.class);
            criteria.add(Restrictions.eq("id", id));
            md = (MdDistribution) criteria.uniqueResult();

            md.setMdMetadataId(mdModel.getMdMetadataId());

            session.update(md);
            trx.commit();

            return "berhasil diubah.....!!";
        } catch (Exception e) {
            trx.rollback();
            return "Error "+e.getMessage();
        } finally {
            //session.close();
        }
    }
}
